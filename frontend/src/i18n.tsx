type I18nContextValue = {
  language: LanguageCode
  setLanguage: (language: LanguageCode) => void
  msg: <T = string>(key: string, params?: TranslationParams) => T
}

const I18nContext = createContext<I18nContextValue | null>(null)

function loadInitialLanguage(): LanguageCode {
  try {
    const raw = localStorage.getItem(STORAGE_KEY)
    if (raw === 'en' || raw === 'zh' || raw === 'ko') return raw
  } catch {}
  return 'en'
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<LanguageCode>(loadInitialLanguage)

  useEffect(() => {
    try {
      localStorage.setItem(STORAGE_KEY, language)
    } catch {}
  }, [language])

  const value = useMemo<I18nContextValue>(() => ({
    language,
    setLanguage,
    msg: <T,>(key: string, params?: TranslationParams): T => {
      const table = MESSAGES[language] ?? MESSAGES.en
      const fallback = MESSAGES.en[key]
      const value = table[key] ?? fallback ?? key
      if (typeof value === 'function') {
        return value(params ?? {}) as T
      }
      return value as T
    },
  }), [language])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const context = useContext(I18nContext)
  if (!context) throw new Error('useI18n must be used within I18nProvider')
  return context
}
